#include "libft.h"

char	*ft_strncat(char *restrict s1, const char *restrict s2, size_t n)
{
	size_t	i;

	i = 0;
	while (s1[i])
		i++;
	while (n > 0 && *s2)
	{
		s1[i] = *s2;
		s2++;
		i++;
		n--;
	}
	s1[i] = '\0';
	return (s1);
}
